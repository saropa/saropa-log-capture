// @ts-check
/**
 * Measures MANIFEST translation coverage and regenerates src/l10n/nls-coverage-data.ts.
 *
 * `verify-nls.js` only proves KEY parity (every %key% present in every locale). It cannot
 * see that a key is present but still holds the English source value — an untranslated
 * placeholder. This audit measures the gap `verify-nls` is blind to: for each
 * package.nls.<locale>.json, how many values actually differ from package.nls.json (English).
 *
 * The measured per-locale percent is written into a TS data module that the runtime imports,
 * so the activation-time "menus are partly English" notice can never claim more coverage than
 * was actually measured. The notice is informational only — this audit never gates the build
 * on low coverage (the project's policy is notify, not gate); --check only fails when the
 * committed data file is STALE relative to the locale files.
 *
 *   node scripts/modules/generate/nls-coverage.mjs          # rewrite data + print table
 *   node scripts/modules/generate/nls-coverage.mjs --check  # CI: fail if data file is stale
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..", "..", "..");
const outRel = "src/l10n/nls-coverage-data.ts";
const outPath = path.join(root, outRel);

const englishName = "package.nls.json";

/** List package.nls.<locale>.json files (excludes the English base package.nls.json). */
function findLocaleFiles() {
	return fs
		.readdirSync(root)
		.filter((f) => f.startsWith("package.nls.") && f.endsWith(".json") && f !== englishName)
		.sort();
}

/** Locale tag from a filename: package.nls.pt-br.json -> "pt-br". */
function localeOf(file) {
	return file.slice("package.nls.".length, -".json".length);
}

/**
 * Coverage for one locale: a key counts as translated when its value is a non-empty
 * string that differs from the English source. Keys missing from English are ignored
 * (parity is verify-nls.js's job); we measure value translation against the English set.
 */
function coverageFor(english, locale) {
	const keys = Object.keys(english);
	let translated = 0;
	for (const k of keys) {
		const en = english[k];
		const loc = locale[k];
		if (typeof loc === "string" && loc.length > 0 && loc !== en) {
			translated += 1;
		}
	}
	const total = keys.length;
	const pct = total === 0 ? 100 : Math.round((translated / total) * 100);
	return { translated, total, pct };
}

const english = JSON.parse(fs.readFileSync(path.join(root, englishName), "utf8"));
const localeFiles = findLocaleFiles();

/** @type {{ locale: string; translated: number; total: number; pct: number }[]} */
const rows = [];
for (const file of localeFiles) {
	const locale = JSON.parse(fs.readFileSync(path.join(root, file), "utf8"));
	const { translated, total, pct } = coverageFor(english, locale);
	rows.push({ locale: localeOf(file), translated, total, pct });
}

/** Render the generated TS data module (a literal map: locale tag -> coverage percent). */
function renderModule() {
	const lines = [
		"// AUTO-GENERATED by scripts/modules/generate/nls-coverage.mjs — do not edit by hand.",
		"// Run `npm run generate:nls-coverage` after changing any package.nls*.json file.",
		"//",
		"// Percent of MANIFEST strings (command titles, settings, menus) actually translated",
		"// per locale, measured as values that differ from the English source. The runtime",
		"// activation notice reads this to honestly tell users when chrome is largely English.",
		"// English is the source and is always 100%.",
		"",
		"/** Manifest translation coverage (0-100) keyed by VS Code display-language tag. */",
		"export const nlsManifestCoverage: Readonly<Record<string, number>> = {",
		"    en: 100,",
	];
	for (const r of rows) {
		lines.push(`    "${r.locale}": ${r.pct},`);
	}
	lines.push("};", "");
	return lines.join("\n");
}

const body = renderModule();

/** Print the human-readable audit table (the "which languages are truly useful" view). */
function printTable() {
	console.log("\nmanifest translation coverage (package.nls.<locale>.json)");
	console.log("  locale   translated  total  coverage");
	console.log("  ------------------------------------");
	for (const r of rows) {
		console.log(
			`  ${r.locale.padEnd(7)} ${String(r.translated).padStart(9)}  ${String(r.total).padStart(5)}  ${String(r.pct).padStart(6)}%`,
		);
	}
	console.log("  ------------------------------------");
	console.log("  Note: 100% key parity is enforced separately by verify-nls.js; this is VALUE coverage.\n");
}

const check = process.argv.includes("--check");
if (check) {
	if (!fs.existsSync(outPath)) {
		console.error(`ERROR: ${outRel} missing. Run: npm run generate:nls-coverage`);
		process.exit(1);
	}
	const existing = fs.readFileSync(outPath, "utf8");
	if (existing !== body) {
		console.error(`ERROR: ${outRel} is out of date. Run: npm run generate:nls-coverage`);
		process.exit(1);
	}
	console.log(`OK: ${outRel} matches package.nls*.json.`);
} else {
	fs.writeFileSync(outPath, body, "utf8");
	console.log(`Wrote ${outRel} (${rows.length} locales).`);
	printTable();
}
