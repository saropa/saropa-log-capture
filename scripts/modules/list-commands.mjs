// @ts-check
/**
 * Generates doc/internal/contributes-commands.md from package.json contributes.commands.
 *
 *   node scripts/modules/list-commands.mjs
 *   node scripts/modules/list-commands.mjs --check
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..", "..");
const outRel = "doc/internal/contributes-commands.md";
const outPath = path.join(root, outRel);

const pkg = JSON.parse(fs.readFileSync(path.join(root, "package.json"), "utf8"));
/** @type {{ command?: string; title?: string; category?: string; icon?: string }[]} */
const cmds = Array.isArray(pkg.contributes?.commands) ? pkg.contributes.commands : [];

function escCell(s) {
	return String(s ?? "")
		.replace(/\\/g, "\\\\")
		.replace(/\|/g, "\\|")
		.replace(/\r?\n/g, " ");
}

const rows = [...cmds]
	.filter((c) => c && typeof c.command === "string")
	.sort((a, b) => a.command.localeCompare(b.command));

const lines = [
	"# Extension command IDs (`contributes.commands`)",
	"",
	"<!-- AUTO-GENERATED by scripts/modules/list-commands.mjs — do not edit by hand. -->",
	"",
	"Reference for keybinding `command` values and automation. Titles are raw `package.json` strings (often `%command.*.title%` NLS keys).",
	"",
	"| Command ID | Title | Category |",
	"|------------|-------|----------|",
];
for (const c of rows) {
	lines.push(`| \`${escCell(c.command)}\` | ${escCell(c.title)} | ${escCell(c.category)} |`);
}
lines.push("", `**Total:** ${rows.length} commands.`, "");
const body = lines.join("\n");

const check = process.argv.includes("--check");
if (check) {
	if (!fs.existsSync(outPath)) {
		console.error(`ERROR: ${outRel} missing. Run: node scripts/modules/list-commands.mjs`);
		process.exit(1);
	}
	const existing = fs.readFileSync(outPath, "utf8");
	if (existing !== body) {
		console.error(`ERROR: ${outRel} is out of date. Run: node scripts/modules/list-commands.mjs`);
		process.exit(1);
	}
	console.log(`OK: ${outRel} matches package.json.`);
} else {
	fs.mkdirSync(path.dirname(outPath), { recursive: true });
	fs.writeFileSync(outPath, body, "utf8");
	console.log(`Wrote ${outRel} (${lines.length} lines).`);
}
