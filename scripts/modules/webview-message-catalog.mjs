// @ts-check
/**
 * Generates doc/internal/webview-incoming-message-types.md from TypeScript handler sources.
 * Incoming = Log Viewer (and related) webview postMessage `type` values handled in extension host.
 *
 * Usage:
 *   node scripts/modules/webview-message-catalog.mjs           # write doc
 *   node scripts/modules/webview-message-catalog.mjs --check  # exit 1 if doc differs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..", "..");
const outRel = "doc/internal/webview-incoming-message-types.md";
const outPath = path.join(root, outRel);

/** @param {string} text */
function extractCaseTypes(text) {
	const set = new Set();
	const re = /\bcase\s+"([^"]+)"/g;
	let m;
	while ((m = re.exec(text)) !== null) {
		set.add(m[1]);
	}
	return set;
}

/** e.g. msg.type === 'trackInteraction' or type === "setMinimapCustomPx" */
function extractEqualityTypes(text) {
	const set = new Set();
	const re = /\b(?:msg\.)?type\s*===\s*['"]([^'"]+)['"]/g;
	let m;
	while ((m = re.exec(text)) !== null) {
		set.add(m[1]);
	}
	return set;
}

/** Keys from SAROPA_BOOL_SETTING_BY_MSG_TYPE (boolean workspace toggles from webview). */
function extractBoolMapMessageTypes() {
	const p = path.join(root, "src/ui/provider/viewer-workspace-bool-message-map.ts");
	const text = fs.readFileSync(p, "utf8");
	const set = new Set();
	const re = /^\s{2}([A-Za-z0-9_]+):\s*"/gm;
	let m;
	while ((m = re.exec(text)) !== null) {
		set.add(m[1]);
	}
	return set;
}

function buildMarkdown() {
	const providerDir = path.join(root, "src/ui/provider");
	const handlerFiles = fs
		.readdirSync(providerDir)
		.filter((f) => f.startsWith("viewer-message-handler") && f.endsWith(".ts"))
		.sort();

	/** @type {Map<string, Set<string>>} */
	const byType = new Map();

	function note(type, file) {
		if (!byType.has(type)) {
			byType.set(type, new Set());
		}
		byType.get(type).add(file);
	}

	for (const file of handlerFiles) {
		const text = fs.readFileSync(path.join(providerDir, file), "utf8");
		for (const t of extractCaseTypes(text)) {
			note(t, file);
		}
		for (const t of extractEqualityTypes(text)) {
			note(t, file);
		}
	}

	for (const t of extractBoolMapMessageTypes()) {
		note(t, "viewer-workspace-bool-message-map.ts (boolean toggles → config update)");
	}

	const types = [...byType.keys()].sort((a, b) => a.localeCompare(b));

	const lines = [
		"# Log Viewer — webview → extension `postMessage` types",
		"",
		"<!-- AUTO-GENERATED by scripts/modules/webview-message-catalog.mjs — do not edit by hand. -->",
		"",
		"Each row is a `type` string sent from the log viewer webview (or embedded scripts registered with the same dispatcher).",
		"Handlers live under `src/ui/provider/viewer-message-handler*.ts` and `dispatchViewerMessage` in `viewer-message-handler.ts`.",
		"",
		"| `type` | Referenced in |",
		"|--------|----------------|",
	];

	for (const t of types) {
		const files = [...(byType.get(t) ?? [])].sort();
		const esc = t.replace(/\|/g, "\\|");
		lines.push(`| \`${esc}\` | ${files.join(", ")} |`);
	}

	lines.push("", `**Total:** ${types.length} distinct types.`, "");
	return lines.join("\n");
}

const check = process.argv.includes("--check");
const body = buildMarkdown();

if (check) {
	if (!fs.existsSync(outPath)) {
		console.error(`ERROR: ${outRel} missing. Run: node scripts/modules/webview-message-catalog.mjs`);
		process.exit(1);
	}
	const existing = fs.readFileSync(outPath, "utf8");
	if (existing !== body) {
		console.error(`ERROR: ${outRel} is out of date. Run: node scripts/modules/webview-message-catalog.mjs`);
		process.exit(1);
	}
	console.log(`OK: ${outRel} matches handler sources.`);
} else {
	fs.mkdirSync(path.dirname(outPath), { recursive: true });
	fs.writeFileSync(outPath, body, "utf8");
	console.log(`Wrote ${outRel} (${body.split("\n").length} lines).`);
}
