// @ts-check
/**
 * Generates doc/internal/webview-outbound-message-types.md from TypeScript sources.
 * Outbound = extension host → log viewer webview messages (`postMessage` / `post` payloads with `type`).
 *
 * Heuristic: within 800 chars after `{` following postMessage/post(, first `type: 'x' | type: "x"`.
 * Rare multiline gaps larger than the window may be omitted — widen `WINDOW` if needed.
 *
 * Usage:
 *   node scripts/modules/webview-host-outbound-catalog.mjs
 *   node scripts/modules/webview-host-outbound-catalog.mjs --check
 */
import fs, { globSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..", "..");
const outRel = "doc/internal/webview-outbound-message-types.md";
const outPath = path.join(root, outRel);

const WINDOW = 800;
/* Do not match bare `postMessage(` — that appears inside embedded webview script strings in *.ts. */
const PATTERNS = [
	{ re: new RegExp(`(?:this|target)\\.postMessage\\s*\\(\\s*\\{[\\s\\S]{0,${WINDOW}}?\\btype\\s*:\\s*['"]([^'"]+)['"]`, "g") },
	{ re: new RegExp(`ctx\\.post\\s*\\(\\s*\\{[\\s\\S]{0,${WINDOW}}?\\btype\\s*:\\s*['"]([^'"]+)['"]`, "g") },
	{ re: new RegExp(`\\.webview\\.postMessage\\s*\\(\\s*\\{[\\s\\S]{0,${WINDOW}}?\\btype\\s*:\\s*['"]([^'"]+)['"]`, "g") },
];

/** @param {string} filePath */
function extractTypes(filePath) {
	const text = fs.readFileSync(filePath, "utf8");
	/** @type {Map<string, Set<string>>} */
	const byType = new Map();
	for (const { re } of PATTERNS) {
		const r = new RegExp(re.source, re.flags);
		let m;
		while ((m = r.exec(text)) !== null) {
			const t = m[1];
			if (!t) {
				continue;
			}
			if (!byType.has(t)) {
				byType.set(t, new Set());
			}
			byType.get(t).add(path.relative(root, filePath).replace(/\\/g, "/"));
		}
	}
	return byType;
}

function buildMarkdown() {
	const files = globSync("src/ui/**/*.ts", {
		cwd: root,
		exclude: (p) => {
			const n = p.replace(/\\/g, "/");
			return n.includes("/viewer/") || n.includes("/test/");
		},
	});

	/** @type {Map<string, Set<string>>} */
	const merged = new Map();
	for (const rel of files) {
		const abs = path.join(root, rel);
		const part = extractTypes(abs);
		for (const [t, set] of part) {
			if (!merged.has(t)) {
				merged.set(t, new Set());
			}
			for (const s of set) {
				merged.get(t).add(s);
			}
		}
	}

	const types = [...merged.keys()].sort((a, b) => a.localeCompare(b));
	const lines = [
		"# Log Viewer — extension → webview `postMessage` types",
		"",
		"<!-- AUTO-GENERATED by scripts/modules/webview-host-outbound-catalog.mjs — do not edit by hand. -->",
		"",
		"Heuristic index of `type` fields on objects passed to `this.postMessage` / `target.postMessage` / `ctx.post` / `.webview.postMessage` from `src/ui/**/*.ts` (excluding `src/ui/viewer/` and tests).",
		"",
		"| `type` | Referenced in |",
		"|--------|----------------|",
	];
	for (const t of types) {
		const filesList = [...(merged.get(t) ?? [])].sort();
		const esc = t.replace(/\|/g, "\\|");
		lines.push(`| \`${esc}\` | ${filesList.join(", ")} |`);
	}
	lines.push("", `**Total:** ${types.length} distinct types.`, "");
	return lines.join("\n");
}

const check = process.argv.includes("--check");
const body = buildMarkdown();

if (check) {
	if (!fs.existsSync(outPath)) {
		console.error(`ERROR: ${outRel} missing. Run: node scripts/modules/webview-host-outbound-catalog.mjs`);
		process.exit(1);
	}
	const existing = fs.readFileSync(outPath, "utf8");
	if (existing !== body) {
		console.error(`ERROR: ${outRel} is out of date. Run: node scripts/modules/webview-host-outbound-catalog.mjs`);
		process.exit(1);
	}
	console.log(`OK: ${outRel} matches sources.`);
} else {
	fs.mkdirSync(path.dirname(outPath), { recursive: true });
	fs.writeFileSync(outPath, body, "utf8");
	console.log(`Wrote ${outRel} (${body.split("\n").length} lines).`);
}
